# Creating a Dictionary 
dict={1:'Apple',2:'Mango',3:'Banana'}
print(f"Orginal Dictionary {dict}")
print(f"Accessing an Element in Dictionary {dict[1]}") # Accessing Elements in Dictionary
dict[4]='Orange' # Adding an Element in Dictionary
print(f"Updated Dictionary {dict}")
dict[1]='Cat' # Modifing An Element
print(f"Updated Dictionary {dict}")
del dict[2] # Removing An Element from the Dictionary
print(f"Updated Dictionary {dict}")
