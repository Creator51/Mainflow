# Creating a Set
set={1,2,3,4,5,6,7}
print(f"Orginal set {set}")
set.add(8) # Adding an Element into the set
print(f"Updated Set {set}")
set={1,2,2,2,3,3,4,5,5,6,6,7,7} # Set Cannot contain 2 same Elements
print(f"Updated Set {set}")
set.remove(1) # Removing an Element from the set
print(f"Updated Set {set}")